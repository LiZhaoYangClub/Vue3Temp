export const getUrlQuery = () => {
  const params: Recordable = {}
  const query = window.location.search.substring(1)
  const vars = query.split('&')
  for (let i = 0; i < vars.length; i++) {
    const pair = vars[i].split('=')
    params[pair[0]] = pair[1]
  }
  return params
}

